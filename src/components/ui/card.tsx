  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border bg-card text-card-foreground shadow-xl dark:bg-card dark:text-card-foreground dark:border-card dark:shadow-xl",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

